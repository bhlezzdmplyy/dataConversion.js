// Convert a String to a Number
let price = "200";

// Method 1: Using parseInt()
let priceParsedInt = parseInt(price);
console.log("Using parseInt():", priceParsedInt); // Output: 200

// Method 2: Using Number()
let priceNumber = Number(price);
console.log("Using Number():", priceNumber); // Output: 200

// Method 3: Using the + operator
let pricePlusOperator = +price;
console.log("Using + operator:", pricePlusOperator); // Output: 200
